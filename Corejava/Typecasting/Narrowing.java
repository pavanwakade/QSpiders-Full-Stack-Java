package Typecasting;
class NarrowingByteToAll
{
public static void main(String [] args)
	
	{
		
		//all to byte
	double a =1000;
	byte b=(byte)a;
      System.out.println(b);
	 
	  long d =1055;
	  byte e=(byte)d;
   System.out.println(e);
   	 
	  float c =1000;
	  byte f=(byte)c;
      System.out.println(f);
	  
	  int g=1000;
	  byte h=(byte)g;
	  System.out.println(h);
	  
	  short i=1000;
	  byte j=(byte)i;
	  System.out.println(j);
	  
	  char k=1227;
	  byte l=(byte)k;
	  System.out.println(l);
	
	}
}