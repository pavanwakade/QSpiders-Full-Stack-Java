-- CREATE USERS TABLE
CREATE TABLE USERS
(
    USRID CHAR(5),
    UNAME VARCHAR2(20) NOT NULL,
    CONC CHAR(10) NOT NULL,
    EMAIL VARCHAR2(30) NOT NULL,
    ADDRESS VARCHAR2(35) NOT NULL,
    CONSTRAINT PK1 PRIMARY KEY (USRID),
    CONSTRAINT CHK1 CHECK (LENGTH(USRID) = 5),
    CONSTRAINT CHK2 CHECK (EMAIL LIKE '%@%' AND EMAIL NOT LIKE '%@%@%' AND EMAIL LIKE '%@%.COM'),
    CONSTRAINT U1 UNIQUE (EMAIL),
    CONSTRAINT CHK3 CHECK (LENGTH(CONC) = 10),
    CONSTRAINT CHK4 CHECK (SUBSTR(CONC, 1, 1) IN ('6', '7', '8', '9')),
    CONSTRAINT U2 UNIQUE (CONC)
);

