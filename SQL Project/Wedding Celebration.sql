    PAYMENTMETHOD VARCHAR2(25) NOT NULL,
    CONSTRAINT PK8 PRIMARY KEY (PAYMENTID),
    CONSTRAINT FK7 FOREIGN KEY (BKGID) REFERENCES BOOKINGS(BKGID)
);

