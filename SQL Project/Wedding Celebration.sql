-- CREATE BOOKINGS TABLE
CREATE TABLE BOOKINGS
(
    BKGID CHAR(5), 
    USRID CHAR(5) NOT NULL, 
    VNUID CHAR(5) NOT NULL,
    CTRID CHAR(5),
    DCRID CHAR(5),
    PHOID CHAR(5),
    MUSID CHAR(5),
    BKGDATE DATE NOT NULL, 
    EVENTDATE DATE NOT NULL,
    TOTALCOST NUMBER(8, 2),
    CONSTRAINT PK7 PRIMARY KEY (BKGID), 
    CONSTRAINT CHK25 CHECK (LENGTH(BKGID) = 5), 
    CONSTRAINT FK1 FOREIGN KEY (USRID) REFERENCES USERS(USRID), 
    CONSTRAINT FK2 FOREIGN KEY (VNUID) REFERENCES VENUES(VNUID), 
    CONSTRAINT FK3 FOREIGN KEY (CTRID) REFERENCES CATERERS(CTRID), 
    CONSTRAINT FK4 FOREIGN KEY (DCRID) REFERENCES DECORATORS(DCRID), 
    CONSTRAINT FK5 FOREIGN KEY (PHOID) REFERENCES PHOTOGRAPHERS(PHOID), 
    CONSTRAINT FK6 FOREIGN KEY (MUSID) REFERENCES MUSICIANS(MUSID) 
);

-- CREATE PAYMENTS TABLE
